package com.sloth.common;

public class AjaxData {
	private int code;
	private String msg;
	private Object data;
	private static int success_code = 20000;

	public AjaxData() {
		this.code = success_code;
	}

	public AjaxData(int code) {
		this.code = code;
	}

	public AjaxData(String msg) {
		this.code = success_code;
		this.msg = msg;
	}

	public AjaxData(Object data) {
		this.code = success_code;
		this.data = data;
	}

	public AjaxData(int code, String msg) {
		this.code = code;
		this.msg = msg;
	}

	public AjaxData(String msg, Object data) {
		this.msg = msg;
		this.data = data;
	}

	public AjaxData(int code, Object data) {
		this.code = code;
		this.data = data;
	}

	public AjaxData(int code, String msg, Object data) {
		this.code = code;
		this.msg = msg;
		this.data = data;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}
}